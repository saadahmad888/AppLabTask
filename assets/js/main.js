$('#MainBAnnerSlider').owlCarousel({
    loop: true,
    margin: 0,
    nav: false,
    dots: false,
    responsive: {
        0: {
            items: 1
        },
        600: {
            items: 1
        },
        1000: {
            items: 1
        }
    },
    onInitialized: addSlideAnimations,  // Trigger on first load
    onChanged: function () { } // override to prevent flicker
});

var totalSlides = $('#MainBAnnerSlider .owl-item:not(.cloned)').length;

$('#MainBAnnerSlider').on('changed.owl.carousel', function (event) {
    var currentIndex = (event.item.index - event.relatedTarget._clones.length / 2) % totalSlides + 1;
    if (currentIndex <= 0) currentIndex += totalSlides;

    $('.sliderCount .active').text(('0' + currentIndex).slice(-2) + ' -');
    $('.sliderCount .inactive').text(('0' + totalSlides).slice(-2));

    addSlideAnimations(event);
});

// Animation handler
function addSlideAnimations(event) {
    var currentIndex = event.item.index;

    // Remove all active animation classes
    $('#MainBAnnerSlider .animate-from-right, #MainBAnnerSlider .animate-from-bottom').removeClass('active');

    // Find the current visible slide
    setTimeout(function () {
        var $currentSlide = $('#MainBAnnerSlider .owl-item').eq(currentIndex);
        $currentSlide.find('.animate-from-right').addClass('active');
        $currentSlide.find('.animate-from-bottom').addClass('active');
    }, 100); // Add slight delay for smoother animation
}




// Branding Slider Animation
const slider = document.getElementById('BrandingSlider');
let direction = 1; // 1 = right, -1 = left
let position = 0;
let speed = 0.5; // Adjust speed as needed

// Clone images to fill space
const cloneCount = 3; // Clone x times to avoid gaps
for (let i = 0; i < cloneCount; i++) {
    slider.innerHTML += slider.innerHTML;
}

// Get total width of slider content
function getSliderWidth() {
    return slider.scrollWidth / (cloneCount + 1); // One original set
}

function animateSlider() {
    const maxScroll = getSliderWidth();

    position += speed * direction;

    if (position >= maxScroll || position <= 0) {
        direction *= -1; // reverse direction
    }

    slider.style.transform = `translateX(${-position}px)`;

    requestAnimationFrame(animateSlider);
}

animateSlider();






const imageBoxes = document.querySelectorAll('.ivMianBox img');
const lightbox = document.getElementById('lightboxOverlay');
const lightboxImg = document.querySelector('.lightbox-img');
const closeBtn = document.querySelector('.lightbox-close');

imageBoxes.forEach(img => {
    img.addEventListener('click', () => {
        lightboxImg.src = img.src;
        lightbox.classList.add('active');
    });
});

closeBtn.addEventListener('click', () => {
    lightbox.classList.remove('active');
});

// Optional: Click outside to close
lightbox.addEventListener('click', (e) => {
    if (e.target === lightbox || e.target === lightboxImg) return;
    lightbox.classList.remove('active');
});



